    public DetailKnihyForm(Kniha kniha, java.awt.Frame parent) {
        this(parent, true);
        this.kniha = kniha;
        
        Long id = kniha.getId();
        List<Vydavatelstvo> list = vydDao.vydavatelstvoPodlaId(id);
        Vydavatelstvo vyd = list.get(0);
        String vydavatelstvo = vyd.getNazov();
        
        List<Kategoria> listt = katDao.kategoriaPodlaId(id);
        Kategoria kat = listt.get(0);
        String kategoria = kat.getNazov();
        
        lblNazov.setText(kniha.getNazov());
        lblAutor.setText(kniha.getAutor());
        lblCena.setText(kniha.getCena().toString());
        txtAreaRecenzia.setText(kniha.getRecenzia());
        lblKategoria.setText(kategoria);
        lblVydavatelstvo.setText(vydavatelstvo);        
    }
    
