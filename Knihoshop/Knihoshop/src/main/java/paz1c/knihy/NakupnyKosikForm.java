    private void tabZoznamSelectionValueChanged(ListSelectionEvent e) {
        if(!e.getValueIsAdjusting()) {
            if(!tabZoznam.getSelectionModel().isSelectionEmpty()) {
                btnOdstran.setEnabled(true);
            } else {
                btnOdstran.setEnabled(false);
            }
        }
    }

    private void aktualizujZoznamKnihVKosiku() {
        
        if(zoznamKnih != null){
            for(int i = 0; i<zoznamKnih.size(); i++){
                kniha=zoznamKnih.get(i);
                knihaTableModel.dajKnihuSTymtoIdDoKosika(kniha.getId(), zoznamKnih);
            }
        }
    }

