    private void btnOdstranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOdstranActionPerformed
        int vybranyRiadok = tabZoznam.getSelectedRow();
        int vybratyIndexVModeli = tabZoznam.convertRowIndexToModel(vybranyRiadok);
        
        Kniha vybrataKniha
                = knihaTableModel.dajPodlaCislaRiadku(vybratyIndexVModeli);

        if(vybrataKniha == null) {
            return;
        } else {
            for(int i=0; i<zoznamKnih.size(); i++){
                Long idKnihy = zoznamKnih.get(i).getId();
                if(vybrataKniha.getId()==idKnihy){
                    zoznamKnih.remove(i);
                }
            }
        }
        aktualizujZoznamKnihVKosiku();       
    }//GEN-LAST:event_btnOdstranActionPerformed

